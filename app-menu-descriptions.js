export function texteDescCarteJeu(jeuId, niveau) {
  const bloc = PAR_JEU[jeuId];
  if (!bloc) return null;
  let i = ORDRE_CLASSES.indexOf(niveau);
  if (i === -1) i = 0;
  for (let k = i; k >= 0; k--) {
    const t = bloc[ORDRE_CLASSES[k]];
    if (t) return t;
  }
  return null;
}
